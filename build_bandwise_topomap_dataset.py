from scipy.signal import butter, hilbert, sosfiltfilt

BANDS_HZ: list[tuple[float, float]] = [
    (4.0, 8.0),
    (8.0, 12.0),
    (12.0, 16.0),
    (16.0, 20.0),
    (20.0, 24.0),
    (24.0, 28.0),
    (28.0, 32.0),
    (32.0, 40.0),
]

EEG_COORDS: dict[str, tuple[float, float]] = {
    "AF7": (-0.60, 0.90),
    "Fp1": (-0.35, 1.00),
    "Fp2": (0.35, 1.00),
    "AF8": (0.60, 0.90),
    "F3": (-0.50, 0.45),
    "F4": (0.50, 0.45),
    "P3": (-0.50, -0.35),
    "P4": (0.50, -0.35),
    "P7": (-0.90, -0.25),
    "O1": (-0.35, -0.90),
    "O2": (0.35, -0.90),
    "P8": (0.90, -0.25),
}


